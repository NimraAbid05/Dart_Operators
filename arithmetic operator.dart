void main(){

  //arithmetic operators
  int num1=40;
  int num2=30;
  print("Arithmetic Operators");
  print("********************");
   //addition
  print("Addition: ${num1+num2}"); 
   //subtraction
  print("Subtraction: ${num1-num2}"); 
  //product
  print("Product: ${num1*num2}");
   //return float value 
  print("Divide: ${num1/num2}"); 
  //return int result
  print("Divide: ${num1~/num2}");  
  //return remainder
  print("Modulus: ${num1%num2}"); 
  //add 1 in value
  print("Increment of 40 is ${++num1}");
  //sub 1 from value
  print("Decrement of 30 is ${--num2}");

}